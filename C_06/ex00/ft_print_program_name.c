#include <unistd.h>
#include <stdio.h>

void	putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argc, char *argv[])
{
	if (argc == 1)
	{
		putstr(argv[0]);
		putstr("\n");
	}
	return (0);
}
